// Function to try different image extensions if original fails
function tryImageExtensions(img, baseUrl) {
  const extensions = ["png", "jpg", "jpeg", "jfif", "webp"];
  const currentExt = img.split(".").pop().toLowerCase();
  const basePath = img.substring(0, img.lastIndexOf("/") + 1);
  const fileName = img.substring(
    img.lastIndexOf("/") + 1,
    img.lastIndexOf(".")
  );

  // Try current extension first, then others
  const extensionsToTry = [
    currentExt,
    ...extensions.filter((ext) => ext !== currentExt),
  ];

  let currentIndex = 0;
  const tryNext = () => {
    if (currentIndex < extensionsToTry.length) {
      const testImg = new Image();
      testImg.onload = () => {
        // Found working image
        const workingUrl =
          basePath + fileName + "." + extensionsToTry[currentIndex];
        document.querySelectorAll(`img[src="${img}"]`).forEach((imgEl) => {
          imgEl.src = workingUrl;
        });
      };
      testImg.onerror = () => {
        currentIndex++;
        tryNext();
      };
      testImg.src = basePath + fileName + "." + extensionsToTry[currentIndex];
    }
  };
  tryNext();
}

// Function to check and fix image URLs by trying different extensions
async function checkAndFixImageUrl(originalUrl, gameSlug) {
  const basePath = originalUrl.substring(0, originalUrl.lastIndexOf("/") + 1);
  const fileName = originalUrl.substring(
    originalUrl.lastIndexOf("/") + 1,
    originalUrl.lastIndexOf(".")
  );
  const extensions = ["png", "jpg", "jpeg", "jfif", "webp"];
  const currentExt = originalUrl.split(".").pop().toLowerCase();

  // Try current extension first
  const extensionsToTry = [
    currentExt,
    ...extensions.filter((ext) => ext !== currentExt),
  ];

  for (const ext of extensionsToTry) {
    const testUrl = basePath + fileName + "." + ext;
    try {
      const response = await fetch(testUrl, {
        method: "HEAD",
        mode: "no-cors",
      });
      // With no-cors, we can't check status, so try loading the image
      const testImg = new Image();
      const loadPromise = new Promise((resolve, reject) => {
        testImg.onload = () => resolve(ext);
        testImg.onerror = () => reject();
        testImg.src = testUrl;
      });

      try {
        await loadPromise;
        // Found working extension
        if (ext !== currentExt) {
          console.log(
            ` Fixed: ${gameSlug} - Changed from .${currentExt} to .${ext}`
          );
          return testUrl;
        } else {
          // Original extension works
          return originalUrl;
        }
      } catch (e) {
        // Continue to next extension
      }
    } catch (e) {
      // Continue to next extension
    }
  }

  // None worked, return original
  console.warn(` Not found: ${gameSlug} - ${originalUrl}`);
  return originalUrl;
}

// Function to pre-check all images and update URLs
async function preCheckAllImages() {
  const fixedImages = [];
  const failedImages = [];

  console.log(" Checking all image URLs...");
  console.log(`Total images to check: ${additionalGames.length}\n`);

  // Check in batches to avoid overwhelming
  const batchSize = 10;
  for (let i = 0; i < additionalGames.length; i += batchSize) {
    const batch = additionalGames.slice(i, i + batchSize);
    await Promise.all(
      batch.map(async (game) => {
        const fixedUrl = await checkAndFixImageUrl(game.img, game.slug);
        if (fixedUrl !== game.img) {
          fixedImages.push({
            slug: game.slug,
            old: game.img,
            new: fixedUrl,
          });
          game.img = fixedUrl; // Update in memory
        } else {
          // Double check if original works
          const testImg = new Image();
          const loadPromise = new Promise((resolve, reject) => {
            testImg.onload = () => resolve(true);
            testImg.onerror = () => reject();
            testImg.src = game.img;
          });

          try {
            await loadPromise;
          } catch (e) {
            failedImages.push({
              slug: game.slug,
              url: game.img,
            });
          }
        }
      })
    );

    // Small delay between batches
    await new Promise((resolve) => setTimeout(resolve, 100));
  }

  // Log results in a format that can be easily copied
  if (fixedImages.length > 0) {
    console.log("\n" + "=".repeat(60));
    console.log(" FIXED IMAGES - Update these in games-data.js:");
    console.log("=".repeat(60));
    fixedImages.forEach((item) => {
      console.log(`\n${item.slug}:`);
      console.log(`  img: "${item.new}",`);
    });
    console.log("\n" + "=".repeat(60));
  }

  if (failedImages.length > 0) {
    console.log("\n" + "=".repeat(60));
    console.log(" FAILED IMAGES (not found with any extension):");
    console.log("=".repeat(60));
    failedImages.forEach((item) => {
      console.log(`- ${item.slug}: ${item.url}`);
    });
    console.log("\n" + "=".repeat(60));
  }

  console.log(`\n Summary:`);
  console.log(`   Total: ${additionalGames.length}`);
  console.log(`   Fixed: ${fixedImages.length}`);
  console.log(`   Failed: ${failedImages.length}`);
  console.log(
    `   OK: ${
      additionalGames.length - fixedImages.length - failedImages.length
    }`
  );

  // Store results globally for potential export
  window.imageCheckResults = {
    fixed: fixedImages,
    failed: failedImages,
    timestamp: new Date().toISOString(),
  };

  // Auto-update games-data.js file if fixes found
  if (fixedImages.length > 0) {
    console.log("\n" + "=".repeat(60));
    console.log(" AUTO-UPDATE COMMANDS FOR games-data.js:");
    console.log("=".repeat(60));
    console.log("\nCopy these commands and send to AI to update the file:\n");

    fixedImages.forEach((item) => {
      console.log(`search_replace("${item.old}", "${item.new}");`);
    });

    console.log("\n" + "=".repeat(60));
    console.log(`\n Found ${fixedImages.length} images that need fixing.`);
    console.log(
      " Send the search_replace commands above to AI to update games-data.js"
    );
    console.log("=".repeat(60));

    // Store for easy access
    window.imageFixesToApply = fixedImages;
  }

  return {
    fixed: fixedImages,
    failed: failedImages,
  };
}

